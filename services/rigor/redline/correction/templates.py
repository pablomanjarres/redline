#: The exact slot set each script's tail expects. These are the parameter keys the module
#: must supply to ``render_corrected_code``. The names match what the module already passes
#: from its config, so the only new keys the module must add are the ones the kernel needs
#: and the config does not carry: check 4 adds ``gene``, ``unit`` and ``alpha``; checks 6
#: and 8 add ``gene``. The module resolves each and passes the same value to both the kernel
#: call and the injected constants (or ``None`` to both, since the kernel's gene picker is
#: deterministic and both sides then pick identically). Check 3's tracked-group column is
#: not a slot: the tail passes ``None`` and the kernel finds it deterministically.
SLOTS: dict[int, tuple[str, ...]] = {
    1: ("h5ad", "unit", "grouping", "ref", "alt", "gene", "covariates", "alpha"),
    2: ("h5ad", "grouping", "target_group", "markers", "split", "seed"),
    3: ("h5ad", "track", "min", "max", "step", "seed"),
    4: ("h5ad", "interest", "technical", "gene", "unit", "alpha"),
    5: ("h5ad", "unit", "grouping", "ref", "alt", "alpha", "method", "tests"),
    6: ("h5ad", "interest", "covariate", "ref", "alt", "unit", "gene", "alpha"),
    7: ("h5ad", "min", "max", "step", "criterion", "chosen", "seed"),
    8: ("h5ad", "grouping", "ref", "alt", "unit", "gene", "claimed_test", "alpha"),
}


def assemble(check_id: int, injected: dict[str, str]) -> str:
    """Build the full standalone script for ``check_id`` from already-repr'd ``injected``.

    The kernel source is concatenated after the tail substitution, never through it, so a
    ``$`` inside scientific code could not be read as a slot. ``substitute`` raises
    ``KeyError`` on a slot the injected map does not fill.
    """
    cid = int(check_id)
    tail = TEMPLATES[cid].substitute(injected)
    return HEADERS[cid] + PREAMBLE + kernel_source() + tail


