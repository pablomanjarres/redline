"""One runnable Python script per check, 1 through 8, built on the shared kernels.

The reliability decision changed. There is no longer a second statistics implementation
living in these templates. Every corrected number a script prints comes from
``redline.correction.kernels``, the same module the check runs in-process. Each emitted
script inlines that module's source text verbatim (via ``inspect.getsource``) and calls the
kernel with the injected parameters. The engine numbers, the preview, and the downloadable
script then agree by construction, because they are the same code running on the same data
with the same seed.

An emitted script is assembled from five parts, in order:

1. A shebang and a docstring naming the check, what it fixes, and how to run it.
2. ``from __future__ import annotations`` and the light imports (argparse, json).
3. The kernel module source, inlined verbatim. A scientist reading the script sees exactly
   the code Redline ran. The script imports nothing from ``redline``; it is standalone.
4. The injected parameter constants, each written with ``repr()`` so strings, lists, and
   ``None`` round-trip as valid Python literals.
5. A ``main()`` that parses ``--h5ad``, loads the AnnData, calls the kernel, prints a short
   human-readable report, and finally prints, as the last line of stdout:

       REDLINE_RESULT {"original": <n>, "corrected": <n>, ...}

   The JSON keys are exactly that check's ``Evidence.numbers`` keys, so the acceptance
   harness can execute the script and diff its output against what Redline reported. On an
   unsalvageable finding the kernel returns ``corrected: None`` and ``unsalvageable: True``,
   and the script prints exactly that. It never fabricates a number, because the branch
   lives in the kernel and the script prints what the kernel returned.

The parameterized tail is a ``string.Template`` with ``${braced}`` slots. A missing slot is
loud: ``substitute`` raises ``KeyError``. The kernel source is concatenated after the
substitution, not passed through it, so a literal ``$`` in scientific code (there is none
today) could never be mistaken for a slot.
"""

from __future__ import annotations

