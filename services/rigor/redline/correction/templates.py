# The scripts are standalone, so ``from __future__ import annotations`` is hoisted to the
# top of every emitted file (a future import must be the first statement). It is stripped
# here and re-added by PREAMBLE, so it never appears twice.
_KERNEL_SRC = inspect.getsource(kernels).replace("from __future__ import annotations\n", "", 1)


def kernel_source() -> str:
    """The kernel module source, inlined verbatim into every emitted script."""
    return _KERNEL_SRC


